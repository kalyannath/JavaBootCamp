package com.company;
/*
8. Write a program to print my name 10 times
 */
public class Q8Name10 {
    public static void name10(String name) {
        for (int i=1; i<=10; i++) {
            System.out.println(name);
        } // end of the for loop
    } // end of the name10 method

    public static void main(String[] args) {
        name10("Kalyan");
    } // end of the main method
} // end of the Name10 class